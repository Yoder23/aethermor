#   - landauer_gap: How far is each element from the thermodynamic limit?
#   - design_space: Sweep parameters, find Pareto-optimal configurations
#   - regime_map: Where do thermodynamic effects dominate vs. classical?
#   - thermal_map: Spatial thermal analysis and hotspot prediction
#   - tech_roadmap: Project paradigm viability across technology nodes
#   - thermal_optimizer: Find optimal compute density distributions
